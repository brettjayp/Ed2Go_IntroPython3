print('a for loop using range(1, 6)')
for number in range(1, 6):
  print(f'number is {number}')

print('a for loop using range(6)')
for integer in range(6):
  print(f'integer is {integer}')

print('a for loop using range(5, 0, -1)')
for number in range(5, 0, -1):
  print(f'number is {number}')

print('a for loop using range(0, 10, 2)')
for integer in range(0, 10, 2):
  print(f'integer is {integer}')